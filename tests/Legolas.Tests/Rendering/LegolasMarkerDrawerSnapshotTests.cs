    /// <summary>
    /// B4 (review-iteration-2): every other fixture in this class is single-
    /// marker — none exercise <see cref="MarkerSceneRenderer.Render"/>'s
    /// for-loop. A bug that drops every-other marker, breaks iteration order,
    /// or silently drops markers with an unregistered style (per the v1
    /// design: <c>_missingDrawerLogged.TryAdd</c> + counter increment, no
    /// exception) would never surface. This fixture renders THREE distinct
    /// Survey pins — one Halo-active and two plain — at distinct pixels and
    /// asserts byte-parity against a <c>PinSceneRenderer</c> baseline that
    /// renders the equivalent <see cref="PinScene"/> (SurveyPins list of
    /// three points + ActivePinIndex = 0). Iteration order matters: the
    /// active pin is rendered LAST by <c>PinSceneRenderer.DrawSurveyPins</c>
    /// (so the halo can't be occluded by neighbouring pins), so the marker
    /// list also places the active pin last.
    /// </summary>
    [SkippableFact]
    public void Multi_marker_survey_dispatch_byte_matches_PinSceneRenderer()
    {
        var spec = new ActivePinTreatmentSpec(
            Treatment: ActivePinTreatment.Halo,
            Color: WhiteStroke,
            HaloPaddingPx: 3.0,
            StrokeThickness: 2.0,
            GlowBlurRadius: 12.0);

        // Three pins at distinct pixels. Pin index 2 (last) is the active
        // one — matches PinSceneRenderer.DrawSurveyPins's "non-active first,
        // then active on top" ordering.
        var pinPixels = new[]
        {
            new PixelPoint(80, 100),
            new PixelPoint(160, 100),
            new PixelPoint(120, 180),
        };
        var scene = BuildMultiPinSurveyScene(pinPixels, activeIndex: 2, treatment: spec);

        // Marker list mirrors that order. Two plain markers, then the
        // active one with the treatment.
        var markers = new List<(PixelPoint, IMarkerStyle)>
        {
            (pinPixels[0], new LegolasSurveyMarkerStyle(
                SurveyOuterStyle(), SurveyCenterStyle(), 24.0, ActiveTreatment: null)),
            (pinPixels[1], new LegolasSurveyMarkerStyle(
                SurveyOuterStyle(), SurveyCenterStyle(), 24.0, ActiveTreatment: null)),
            (pinPixels[2], new LegolasSurveyMarkerStyle(
                SurveyOuterStyle(), SurveyCenterStyle(), 24.0, ActiveTreatment: spec)),
        };

        RunSnapshotComparison(fixtureName: "multi_marker_survey", scene: scene, markers: markers);
    }

