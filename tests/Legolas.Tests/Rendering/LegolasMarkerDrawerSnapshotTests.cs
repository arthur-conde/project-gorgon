/// <summary>
/// Byte-parity snapshot tests for the #835-step-2 marker drawers. Each test
/// builds a deterministic <see cref="PinScene"/> fixture, renders it via the
/// existing <see cref="PinSceneRenderer"/> to a baseline PNG, then builds the
/// equivalent marker list + style set, renders via the new
/// <see cref="MarkerSceneRenderer"/> + Legolas drawers to a second PNG, and
/// asserts the two PNG byte streams are identical against the **checked-in
/// baseline on disk** (never against a freshly-regenerated baseline at
/// run-time — see B1 fix below).
///
/// <para><b>Dual role for these tests.</b> This PR (step 2) uses them as a
/// parity guard &#8212; PinSceneRenderer is the source of truth and the new
/// drawers must reproduce it. Step 6 will delete PinSceneRenderer; the same
/// tests then become regression guards against the checked-in baseline PNGs,
/// catching any later drift in the new drawer implementation.</para>
///
/// <para><b>B1 (review-iteration-2): no auto-regen-on-missing.</b> If a
/// baseline PNG is missing from disk the test fails hard with a
/// <see cref="FileNotFoundException"/> directing the developer to set
/// <c>MITHRIL_REGEN_SNAPSHOTS=1</c> and re-run. With the env var set, the
/// test writes the regenerated baseline and throws so the run still fails —
/// the developer is forced to re-run *without* the env var to verify both
/// PinSceneRenderer and the new drawer match the freshly-regenerated bytes.
/// The old "silently write on first run + ReferenceEquals shortcut" path
/// was a tautological-pass trapdoor: only `new drawer == current
/// PinSceneRenderer` was asserted; the post-step-6 regression-guard
/// contract was defeated. Drift-check is now unconditional.</para>
///
/// <para><b>Headless CI gating.</b>
/// <see cref="HeadlessD2DRenderTarget.TryCreate"/> attempts hardware D3D11
/// first, then WARP. If both fail the test is *Skipped* (not failed) via
/// <see cref="Skip.If(bool, string)"/> from <c>Xunit.SkippableFact</c>, with
/// the captured driver-construction exception's message in the Skip
/// diagnostic. Per scaffold PR #850's note that headless CI may reject
/// D3D11 device creation entirely. Truly headless CI without WARP shows up
/// as "Skipped" in the runner, never as a silent green pass.</para>
///
/// <para><b>Calibration drawer absent.</b> Per the issue body for #835,
/// today's calibration markers are drawn by a WPF <c>ItemsControl</c>
/// (per the <c>#495</c> comment in <see cref="PinSceneRenderer"/>), not by
/// the D2D renderer. There is no <c>PinSceneRenderer</c> branch to
/// byte-parity-compare against, so the calibration drawer ships in step 5
/// when the calibration markers switch over to D2D.</para>
/// </summary>
public sealed class LegolasMarkerDrawerSnapshotTests
{
    private const int CanvasWidth = 240;
    private const int CanvasHeight = 240;

    /// <summary>Env-var flag that opts the developer into baseline regeneration.
