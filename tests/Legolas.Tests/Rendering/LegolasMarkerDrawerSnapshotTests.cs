    private static void RunSnapshotComparison(
        string fixtureName,
        PinScene scene,
        IReadOnlyList<(PixelPoint, IMarkerStyle)> markers)
    {
        // R-C1 (review-iteration-2): real Skip, not a failed assertion. If
        // no D3D11 driver is available the runner reports "Skipped: <reason>"
        // and the captured driver-construction exception goes into the
        // diagnostic so investigators see the actual cause (B2). Pre-R-C1
        // this was a Should().NotBeNull which produced a red failed test.
        using var baselineRt = HeadlessD2DRenderTarget.TryCreate(
            CanvasWidth, CanvasHeight, out var baselineDriverError);
        Skip.If(baselineRt is null, SkipNoD3DPrefix + (baselineDriverError?.Message ?? "(unknown)"));

        var freshBaselinePng = RenderBaselinePng(baselineRt!, scene);

        // B1 (review-iteration-2): the on-disk baseline is the single source
        // of truth. If it's missing, hard-fail (or write-and-fail under the
        // env-var). Drift check is unconditional below — no ReferenceEquals
        // shortcut that lets a freshly-written baseline pass tautologically.
        var baselinePath = ResolveBaselinePath(fixtureName);
        var baselinePng = LoadBaselineOrFail(baselinePath, freshBaselinePng);

        // Drift gate: PinSceneRenderer must still produce the same bytes as
        // the checked-in baseline today. Unconditional — runs even when the
        // env-var regen just wrote the fresh bytes, because the re-run
        // *without* the env-var is the developer's verification step.
        freshBaselinePng.Should().Equal(baselinePng,
            "PinSceneRenderer's output drifted from the checked-in baseline at "
            + baselinePath + " — if this is intentional (e.g. a bug fix in the "
            + "legacy renderer during the migration window), regenerate the "
            + "baseline by setting " + RegenEnvVar + "=1 and re-running this test, "
            + "then re-run again without the env var to verify.");

        using var newRt = HeadlessD2DRenderTarget.TryCreate(
            CanvasWidth, CanvasHeight, out var newDriverError);
        Skip.If(newRt is null, SkipNoD3DPrefix + (newDriverError?.Message ?? "(unknown)"));

        var newPng = RenderNewPipelinePng(newRt!, markers);

        newPng.Should().Equal(baselinePng,
            "the new marker drawer must produce byte-identical D2D output to "
            + "the legacy PinSceneRenderer; this is the parity guarantee that lets "
            + "step 6 retire PinSceneRenderer without visual regression. "
            + "Snapshot test also serves post-step-6 as regression guard against the "
            + "checked-in baseline at " + baselinePath
            + " (see class-level docs).");
    }

    /// <summary>
    /// Resolve the absolute path to the checked-in baseline PNG for the
    /// given fixture. We read from the source tree (relative to
    /// <see cref="AppContext.BaseDirectory"/>) so a regeneration under the
    /// env-var updates the checked-in copy directly.
    /// <c>AppContext.BaseDirectory</c> is <c>bin/Debug/net10.0-windows/</c>,
    /// three "..": <c>tests/Legolas.Tests/Rendering/Snapshots/Baselines/</c>.
    /// </summary>
    private static string ResolveBaselinePath(string fixtureName)
    {
        var dllDir = AppContext.BaseDirectory;
        var baselineDir = System.IO.Path.GetFullPath(
            System.IO.Path.Combine(dllDir, "..", "..", "..", "Rendering", "Snapshots", "Baselines"));
        System.IO.Directory.CreateDirectory(baselineDir);
        return System.IO.Path.Combine(baselineDir, fixtureName + ".png");
    }

    /// <summary>
    /// B1 (review-iteration-2): load the baseline PNG from disk. If missing,
    /// fail loud. If missing AND the <see cref="RegenEnvVar"/> env-var is
    /// set to "1", write the supplied fresh bytes to disk *and* throw — the
    /// developer must re-run without the env-var to actually verify parity
    /// against the just-written baseline. Auto-write-then-pass on first run
    /// (the pre-iteration-2 behaviour) was a tautological-pass trapdoor:
    /// only `new drawer == current PinSceneRenderer` was asserted in that
    /// path, defeating the post-step-6 regression-guard contract.
    /// </summary>
    private static byte[] LoadBaselineOrFail(string path, byte[] freshBaselinePng)
    {
        if (System.IO.File.Exists(path))
        {
            return System.IO.File.ReadAllBytes(path);
        }

        if (Environment.GetEnvironmentVariable(RegenEnvVar) == "1")
        {
            System.IO.File.WriteAllBytes(path, freshBaselinePng);
            throw new Xunit.Sdk.XunitException(
                "Regenerated missing baseline at " + path
                + ". Re-run WITHOUT " + RegenEnvVar
                + "=1 to verify that both PinSceneRenderer and the new drawer "
                + "produce these bytes; the regen run intentionally fails so "
                + "the verify step is not skipped.");
        }

        throw new System.IO.FileNotFoundException(
            "Snapshot baseline missing: " + path
            + ". Set " + RegenEnvVar + "=1 to regenerate, then re-run without it. "
            + "Auto-write-on-missing was removed in #835 review-iteration-2 to "
            + "close the tautological-pass trapdoor (the first run would write "
            + "the current PinSceneRenderer output and assert it against itself).",
            path);
    }

    private static byte[] RenderBaselinePng(HeadlessD2DRenderTarget rt, PinScene scene)
    {
        using var brushes = new D2DBrushCache();
        brushes.Bind(rt.RenderTarget);
        rt.RenderTarget.BeginDraw();
        rt.RenderTarget.Clear(new Color4(0, 0, 0, 0));
        PinSceneRenderer.Render(scene, rt.RenderTarget, rt.Factory, brushes);
        rt.RenderTarget.EndDraw();
        return rt.EncodePng();
    }

    private static byte[] RenderNewPipelinePng(
        HeadlessD2DRenderTarget rt,
        IReadOnlyList<(PixelPoint, IMarkerStyle)> markers)
    {
        var sceneRenderer = new MarkerSceneRenderer();
        LegolasOverlayDrawerRegistrations.RegisterAll(sceneRenderer);

        using var brushes = new D2DBrushCache();
        brushes.Bind(rt.RenderTarget);
        rt.RenderTarget.BeginDraw();
        rt.RenderTarget.Clear(new Color4(0, 0, 0, 0));
        sceneRenderer.Render(markers, rt.RenderTarget, rt.Factory, brushes);
        rt.RenderTarget.EndDraw();
        return rt.EncodePng();
    }
